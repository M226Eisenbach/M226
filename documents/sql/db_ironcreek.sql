DROP DATABASE IF EXISTS db_ironcreek;

CREATE DATABASE db_ironcreek;

USE db_ironcreek;

CREATE TABLE tb_user (
	pk_user INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(20) NOT NULL,
    email VARCHAR(50) NOT NULL,
    userpassword VARCHAR(25) NOT NULL
    );
    
CREATE TABLE tb_tasks (
	pk_task INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    priority TINYINT NOT NULL,
    task VARCHAR(512) NOT NULL,
    notes VARCHAR(512),
    duedate DATE
    );
    
CREATE TABLE tb_user_taks (
	fK_user INT NOT NULL,
    fK_task INT NOT NULL,
    CONSTRAINT tbl_user_tasks_to_user
    FOREIGN KEY (fk_user) REFERENCES tb_user (pk_user)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
    CONSTRAINT tbl_user_tasks_to_tasks
    FOREIGN KEY (fk_task) REFERENCES tb_tasks (pk_task)
	ON DELETE CASCADE
    ON UPDATE CASCADE
	);
    
CREATE TABLE tb_attachements (
	pk_attachement INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    attachementname VARCHAR(255),
    filepath VARCHAR(255),
    fk_task INT NOT NULL,
    CONSTRAINT tbl_attachements_to_tasks
    FOREIGN KEY (fk_task) REFERENCES tb_tasks (pk_task)
	ON DELETE CASCADE
    ON UPDATE CASCADE
    );


